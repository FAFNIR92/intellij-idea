public class Comparate {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        boolean resultadoIgualdad = (a == b);
        System.out.println("¿a es igual a b? " + resultadoIgualdad);

        int x = 15;
        int y = 10;
        boolean resultadoMayorQue = (x > y);
        System.out.println("¿x es mayor que y? " + resultadoMayorQue);

        int num1 = 20;
        int num2 = 20;
        boolean resultadoMenorOIgualQue = (num1 <= num2);
        System.out.println("¿num1 es menor o igual que num2? " + resultadoMenorOIgualQue);
    }
}
