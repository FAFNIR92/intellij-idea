public class DataTypesExample {
    public static void main(String[] args) {
        // Ejemplos de int
        int entero1 = 5;
        int entero2 = -10;
        int entero3 = 0;

        // Ejemplos de double
        double decimal1 = 3.14;
        double decimal2 = -0.001;
        double decimal3 = 2.0;

        // Ejemplos de boolean
        boolean logico1 = true;
        boolean logico2 = false;
        boolean logico3 = (5 > 10);

        // Ejemplos de char
        char caracter1 = 'a';
        char caracter2 = 'B';
        char caracter3 = '*';

        // Ejemplos de byte
        byte pequeno1 = 20;
        byte pequeno2 = -50;
        byte pequeno3 = 0;

        // Ejemplos de short
        short corto1 = 100;
        short corto2 = -500;
        short corto3 = 0;

        // Ejemplos de long
        long largo1 = 1000000;
        long largo2 = -5000000;
        long largo3 = 0;

        System.out.println("Enteros: " + entero1 + ", " + entero2 + ", " + entero3);
        System.out.println("Decimales: " + decimal1 + ", " + decimal2 + ", " + decimal3);
        System.out.println("Booleanos: " + logico1 + ", " + logico2 + ", " + logico3);
        System.out.println("Caracteres: " + caracter1 + ", " + caracter2 + ", " + caracter3);
        System.out.println("Bytes: " + pequeno1 + ", " + pequeno2 + ", " + pequeno3);
        System.out.println("Shorts: " + corto1 + ", " + corto2 + ", " + corto3);
        System.out.println("Longs: " + largo1 + ", " + largo2 + ", " + largo3);
    }
}
